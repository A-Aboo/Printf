#include "ft_printf.h"

int	ft_putstr(char *c)
{
	int	i;

	if (!c)
		return (write(1, "(null)", 6));
	i = 0;
	while (c[i])
	{
		write(1, &c[i], 1);
		i++;
	}
	return (i);
}
